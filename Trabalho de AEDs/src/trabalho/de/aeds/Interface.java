    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Informe um código válido");
            return;
        }
        if (l.Excluir(Integer.parseInt(jTextField3.getText())) == null) {
            JOptionPane.showMessageDialog(this, "Produto não encontrado");
        } else {
            JOptionPane.showMessageDialog(this, "Produto excluído com sucesso ");
            if (l.atual == null) {
                JOptionPane.showMessageDialog(this, "Lista vazia");
                LimparTela();
            } else {
                ExibirProduto(l.Atual());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (l.Localizar(Integer.parseInt(jTextField3.getText())) == null) {
            JOptionPane.showMessageDialog(this, "Produto não encontrado");
        } else {
            JOptionPane.showMessageDialog(this, "Produto encontrado");
            ExibirProduto(l.Localizar(Integer.parseInt(jTextField3.getText())));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

