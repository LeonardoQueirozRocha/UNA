    public void ExibirProduto(Produto P) {
        if (P != null) {
            jTextField3.setText(Integer.toString(P.codigo));
            jTextField2.setText(String.format("%.2f", P.preco));
            jTextField1.setText(P.nome);
        }
    }

    public void LimparTela() {
        jTextField3.setText("");
        jTextField2.setText("");
        jTextField1.setText("");

    }

